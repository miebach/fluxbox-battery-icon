sub acpi_checks
{
  my ($acpi_binary, $acpi_regex, $error_msg) = @_;
  if( -x $acpi_binary )
  {
    my $test_acpi = `$acpi_binary`;
    if($test_acpi !~ /$acpi_regex/)
    {
      $error_msg = "Can't parse $acpi_binary output.\nExpected is a string with battery %%, i.e.:\nBattery 0: Discharging, 61%%, 02:14:50 remaining";
    }
  }
  else
  {
    $error_msg = $acpi_binary." executable was not found. Consider installing package that provides it.";
  }

  if($error_msg)
  {
    my $dialog = Gtk2::MessageDialog->new (undef,
                                           'destroy-with-parent',
                                           'error',
                                           'close',
                                           $error_msg);
    $dialog->run;
    $dialog->destroy;
    exit( 1 );
}

}

sub update_icon
{
  my $icon = shift;

  my $acpi = `$acpi_binary`;
  chomp $acpi;
  $icon->set_tooltip($acpi);
  
  $acpi =~ /$acpi_regex/;
  my $acpi_perc = $1;

  if($acpi_perc >= 75)
  {
    $icon->set_from_pixbuf($full_pixbuf) ;
    $icon->set_blinking (0);
  }
  elsif($acpi_perc < 75 && $acpi_perc >= 50)
  {
    $icon->set_from_pixbuf($seventy_five_pixbuf) ;
    $icon->set_blinking (0);
  }
  elsif($acpi_perc < 50 && $acpi_perc >= 25)
  {
    $icon->set_from_pixbuf($fifty_pixbuf) ;
    $icon->set_blinking (0);
  }
  elsif($acpi_perc < 25 && $acpi_perc >= 10)
  {
    $icon->set_from_pixbuf($twenty_five_pixbuf) ;
    $icon->set_blinking (0) ;
  }
  elsif($acpi_perc < 10)
  {
    $icon->set_from_pixbuf($empty_pixbuf) ;
    $icon->set_blinking (1) ;
  }
  undef $acpi;
  return 1;
}

sub img_pixbuf
{
  my $img_data = shift;
  my $loader = Gtk2::Gdk::PixbufLoader->new();
  $loader->write( $img_data );
  $loader->close();
  return $loader->get_pixbuf();
}


